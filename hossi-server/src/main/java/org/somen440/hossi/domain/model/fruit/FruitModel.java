package org.somen440.hossi.domain.model.fruit;

public class FruitModel {

  public int id;
  public String name;
  public String description;

  public FruitModel() {}

  public FruitModel(int id, String name, String description) {
    this.id = id;
    this.name = name;
    this.description = description;
  }
}
